    private void jBtAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtAceptarActionPerformed
        // TODO add your handling code here:
        //grabar el objeto cliente

        // instanciar el cliente
        Cliente objCliente = new Cliente(this.jTextCedula.getText(),
                this.jTextNombre.getText(),this.jTextmail.getText());
        // validaci√≥n en el nivel logica
        if (objLogCliente.AgregarCliente(objCliente))
            JOptionPane.showMessageDialog(null, "cliente agregadado");
        else
            JOptionPane.showMessageDialog(null, "cliente no se puede agregar");
        
        ArrayList<Cliente> ArrayClientes = new ArrayList<Cliente>();
        try {
            objLogCliente.ConsultarClientes(ArrayClientes);
        } catch (IOException ex) {
            Logger.getLogger(AgregarPedido.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AgregarPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Cliente objCli : ArrayClientes)
            System.out.println(objCli.toString());
    }//GEN-LAST:event_jBtAceptarActionPerformed

